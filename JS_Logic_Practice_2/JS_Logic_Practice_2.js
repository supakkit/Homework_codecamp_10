let Age = +prompt("Input your age : ");

// เขียนคำสั่ง if ที่เช็คอายุว่าอยู่ระหว่าง 18 - 60
if(Age >= 18 && Age <= 60) {
    alert("อายุของคุณอยู่ระหว่าง 18 - 60 ปี");
}

// เขียนคำสั่ง if ที่เช็คอายุว่าไม่อยู่ระหว่าง 18 - 60
if(Age < 18 || Age > 60) {
    alert("อายุของคุณไม่อยู่ระหว่าง 18 - 60 ปี");
}